    private void verificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verificarActionPerformed
        if (archivo && !fragmento.getText().isBlank() && !fragmento.getText().isEmpty()) {
            RabinKarp rk = new RabinKarp();
            String patron = fragmento.getText();

            String verificado = rk.indices(patron.toLowerCase(), texto);
            if (!verificado.isBlank() || !verificado.isEmpty()) {
                MostrarPalabras mp = new MostrarPalabras(verificado);
            } else {
                JOptionPane.showMessageDialog(null, "No se encontraron coincidencias.");
            }
           
            
            

        }
    }//GEN-LAST:event_verificarActionPerformed

